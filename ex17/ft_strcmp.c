int		ft_strcmp(char *s1, char *s2)
{
	return (s1[0] != s2[0] || s1[0] == '\0' || s2[0] == '\0' ?
			(unsigned char)s1[0] - (unsigned char)s2[0] :
			ft_strcmp(s1 + 1, s2 + 1));
}
